import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.ensemble import RandomForestClassifier
from sklearn.pipeline import Pipeline
from sklearn.metrics import accuracy_score
from sklearn.preprocessing import LabelEncoder
import pickle

data = pd.read_csv("D:\Project\Disease Prediction\dataset\lung_cancer.csv")

print(data)

print(data.isnull().sum())

print(list(data.columns))

print(data.info())

le = LabelEncoder()
data["GENDER"] = le.fit_transform(data["GENDER"])
data["LUNG_CANCER"] = le.fit_transform(data["LUNG_CANCER"])

x = data[["AGE","SMOKING","COUGHING","SHORTNESS OF BREATH","CHEST PAIN","WHEEZING"]]
y = data["LUNG_CANCER"]

x_train, x_test, y_train, y_test = train_test_split(
    x, y, test_size=0.2, random_state=42
)

pipeline = Pipeline([
        ("model", RandomForestClassifier(n_estimators=200, random_state=42)),
])

pipeline.fit(x_train, y_train)

y_pred = pipeline.predict(x_test)
acc = accuracy_score(y_test, y_pred)
print(f"Lung Cancer Model Accuracy: {acc * 100:.2f}")

print("\nEnter patient symptoms for lung cancer prediction")

age = int(input("Age: "))
smoking = input("Smoking (yes/no): ").lower()
cough = input("Coughing (yes/no): ").lower()
breath = input("Shortness of breath (yes/no): ").lower()
chest = input("Chest pain (yes/no): ").lower()
wheezing = input("Wheezing (yes/no): ").lower()

symptom_map = {"yes":1, "no":0}

user_data = [[age,symptom_map[smoking],symptom_map[cough],symptom_map[breath],symptom_map[chest],symptom_map[wheezing]]]

pred = pipeline.predict(user_data)

prob = pipeline.predict_proba(user_data)
risk = prob[0][1]

if pred[0] == 1:
    print("\nHigh Risk of Lung Disease")
else:
    print("\nLow Risk of Lung Disease")

print(f"Lung Disease Risk: {risk*100:.2f}%")

pickle.dump(pipeline,open("lung_cancer.pkl","wb"))

