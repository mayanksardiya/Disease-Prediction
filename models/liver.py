import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.ensemble import RandomForestClassifier
from sklearn.pipeline import Pipeline
from sklearn.metrics import accuracy_score
from sklearn.preprocessing import LabelEncoder
import pickle

data = pd.read_csv("D:\Project\Disease Prediction\dataset\liver.csv")

le = LabelEncoder()
data["Gender"] = le.fit_transform(data["Gender"])

data = data.fillna(data.median(numeric_only=True))

#print(list(data.columns))

x = data[["Total_Bilirubin","Alamine_Aminotransferase","Aspartate_Aminotransferase","Albumin"]]
y = data["Dataset"]

x_train, x_test, y_train, y_test = train_test_split(
    x, y, test_size=0.2, random_state=42
)

pipeline = Pipeline(
    steps=[
        ("scaler", StandardScaler()),
        ("model", RandomForestClassifier(n_estimators=200, random_state=42)),
    ]
)

pipeline.fit(x_train, y_train)

#y_pred = pipeline.predict(x_test)
#acc = accuracy_score(y_test, y_pred)
#print(f"Liver Model Accuracy: {acc * 100:.2f}%")

print("\nEnter patient details for liver disease prediction")

tb = float(input("\nTotal Bilirubin: "))
alt = float(input("ALT (Alamine Aminotransferase): "))
ast = float(input("AST (Aspartate Aminotransferase): "))
alb = float(input("Albumin: "))

user_data = [[tb, alt, ast, alb]]

pred = pipeline.predict(user_data)

prob = pipeline.predict_proba(user_data)
risk = prob[0][1]

if risk > 0.7:
    print("\nHigh Risk of Liver disease")
else:
    print("\nLow Risk of Liver disease")

print(f"Liver Disease Risk: {risk*100:.2f}%")

pickle.dump(pipeline,open("liver.pkl","wb"))