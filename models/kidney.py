import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.ensemble import RandomForestClassifier
from sklearn.pipeline import Pipeline
from sklearn.metrics import accuracy_score
from sklearn.preprocessing import LabelEncoder
import pickle

data = pd.read_csv("D:\Project\Disease Prediction\dataset\kidney.csv")

#print(data)

#print(list(data.columns))

#print(data.info())

le = LabelEncoder()

for col in data.columns:
    if data[col].dtype == "object":
        data[col] = le.fit_transform(data[col])

x = data[["age","bp","bgr","bu","sc"]]

y = data["classification"]

x_train, x_test, y_train, y_test = train_test_split(
    x, y, test_size=0.2, random_state=42
)

pipeline = Pipeline([
        ("scaler", StandardScaler()),
        ("model", RandomForestClassifier(n_estimators=200, random_state=42)),
    ]
)

pipeline.fit(x_train, y_train)

#y_pred = pipeline.predict(x_test)
#acc = accuracy_score(y_test, y_pred)
#print(f"Kidney Model Accuracy: {acc * 100}%")

print("Enter patient details for kidney disease prediction")

age = int(input("\nAge: "))
bp = float(input("Blood Pressure: "))
bgr = float(input("Blood Glucose Random: "))
bu = float(input("Blood Urea: "))
sc = float(input("Serum Creatinine: "))

user_data = [[age, bp, bgr, bu, sc]]

pred = pipeline.predict(user_data)

if pred[0] == 1:
    print("\nHigh Risk of Kidney Disease")
else:
    print("\nLow Risk of Kidney Disease")

prob = pipeline.predict_proba(user_data)
risk = prob[0][1]
print(f"Kidney Disease Risk: {risk * 100:.2f}%")

pickle.dump(pipeline,open("kidney.pkl","wb"))