import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.ensemble import RandomForestClassifier
from sklearn.pipeline import Pipeline
from sklearn.metrics import accuracy_score
import pickle

data = pd.read_csv("D:\Project\Disease Prediction\dataset\heart.csv")

#print(data)

#print(data.isnull().sum())

#print(data.describe())

#print(list(data.columns))

c = ["trestbps", "chol", "thalach"]
for col in c:
    if col in data.columns:
        data[col] = data[col].replace(0, np.nan)

data = data.fillna(data.median(numeric_only=True))

x = data[["age","cp","trestbps","chol","thalach","exang"]]
y = data["target"]

x_train, x_test, y_train, y_test = train_test_split(
    x, y, test_size=0.2, random_state=42
)

pipeline = Pipeline(
    steps=[
        ("scaler", StandardScaler()),
        ("model", RandomForestClassifier(n_estimators=200, random_state=42)),
    ]
)

pipeline.fit(x_train, y_train)

#y_pred = pipeline.predict(x_test)
#acc = accuracy_score(y_test, y_pred)
#print(f"Heart Model Accuracy: {acc * 100}%")

print("\nEnter Patient Details For Heart Disease Prediction")

age = int(input("\nAge: "))
cp = int(input("Chest Pain Type (0-3): "))
trestbps = float(input("Resting Blood Pressure: "))
chol = float(input("Cholesterol: "))
thalach = float(input("Max Heart Rate: "))
exang = str(input("Exercise Induced Angina (yes/no): "))

ex_map = {
    "yes" : 1,
    "no" : 0
}

user_data =[[age, cp, trestbps, chol, thalach, ex_map[exang]]]

pred = pipeline.predict(user_data)

if pred[0] == 1:
    print("\nHigh Risk of Heart Disease")
else:
    print("Low Risk of Heart Disease")

prob = pipeline.predict_proba(user_data)
risk = prob[0][1]
print(f"Heart Disease Risk: {risk * 100:.2f}%")

pickle.dump(pipeline,open("heart.pkl","wb"))