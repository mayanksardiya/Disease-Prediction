import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.ensemble import RandomForestClassifier
from sklearn.pipeline import Pipeline
from sklearn.metrics import accuracy_score
import pickle

data = pd.read_csv("D:\Project\Disease Prediction\dataset\diabetes.csv")
#print(data)

#print(data.isnull().sum())

#print(data.describe())

#print(list(data.columns))

c = [
    "Glucose",
    "BloodPressure",
    "SkinThickness",
    "Insulin",
    "BMI",
]
for col in c:
    if col in data.columns:
        data[col] = data[col].replace(0, np.nan)

data = data.fillna(data.median(numeric_only=True))

x = x = data[["Age","Glucose","BMI","DiabetesPedigreeFunction","BloodPressure"]]
y = data["Outcome"]

x_train, x_test, y_train, y_test = train_test_split(
    x, y, test_size=0.2, random_state=42
)

pipeline = Pipeline(
    steps=[
        ("scaler", StandardScaler()),
        ("model", RandomForestClassifier(n_estimators=200, random_state=42)),
    ]
)

pipeline.fit(x_train, y_train)
#y_pred = pipeline.predict(x_test)
#acc = accuracy_score(y_test, y_pred)
#print(f"Diabetes Model Accuracy: {acc * 100}%")

print("Enter patient details for diabetes prediction")
age = int(input("\nAge: "))
glu = float(input("Glucose Level: "))
bmi = float(input("BMI: "))
dpf = float(input("Diabetes Pedigree Function: "))
bp = float(input("Blood Pressure: "))

user_data = [[age ,glu, bmi, dpf, bp]]

prediction = pipeline.predict(user_data)

if prediction[0] == 1:
    print("\nHigh Risk of Diabetes")
else:
    print("\nLow Risk of Diabetes")

prob = pipeline.predict_proba(user_data)
risk = prob[0][1]
print(f"Diabetes Risk: {risk * 100:.2f}%")

pickle.dump(pipeline,open("diabetes.pkl","wb"))